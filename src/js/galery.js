let picture = document.createElement("img");
let actualAlbum;
let actualPicture;

$closeArrow.addEventListener("click", function() {
    $displayDiv.style.display = "none";
});

for (let album of $albums) {
    album.addEventListener("click", function(ev) {
        actualAlbum = albumChooser(ev.target.id);
        $displayDiv.style.display = "flex";
        picture.setAttribute("src",actualAlbum[0]);
        actualPicture = 0;
        $imgDisplay.appendChild(picture);
    });
}

$nextArrow.addEventListener("click", function() {
    if (actualPicture != actualAlbum.length - 1) {
        picture.setAttribute("src",actualAlbum[actualPicture + 1]);
        actualPicture++;
    } else {
        picture.setAttribute("src",actualAlbum[0]);
        actualPicture = 0;
    }
});

$prevArrow.addEventListener("click", function() {
    if (actualPicture != 0 ) {
        picture.setAttribute("src",actualAlbum[actualPicture - 1]);
        actualPicture--;
    } else {
        picture.setAttribute("src",actualAlbum[actualAlbum.length - 1]);
        actualPicture = actualAlbum.length - 1;
    }
});

function albumChooser (id) {
    switch (id) {
        case "album1":
            return album1;
        case "album2":
            return album2;
        case "album3":
            return album3;
        case "album4":
            return album4;
    }
}